// FUNCIONALIDAD DE MOSTRAR Y OCULTAR CAPITULOS EN TEMPORADAS 


document.getElementById('mostrar-ocultar-btn-1').addEventListener('click', function() {
    const capitulosContainer = document.getElementById('capitulos-temporada-1');
    
    if (capitulosContainer.classList.contains('temporada1')) {
      capitulosContainer.classList.remove('temporada1');
      this.textContent = 'Ocultar Episodios';  // Cambia el texto del botón
    } else {
      capitulosContainer.classList.add('temporada1');
      this.textContent = 'Ver Episodios';  // Cambia el texto del botón
    }
  });

